    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
        if(select_id.isSelected(aadhar.getModel())) {
        
            if(checkAadhar(idno.getText())==false) {
                invalid_id.setText("Enter valid Aadhar number");
                invalid_id.setVisible(true);
            }

            else {
                invalid_id.setVisible(false);
                storeBooking();
            }
        }
        
        else if(select_id.isSelected(pan.getModel())) {
            if(checkPan(idno.getText())==false) {
                invalid_id.setText("Enter valid PAN number");
                invalid_id.setVisible(true);
            }

            else {
                invalid_id.setVisible(false);
                storeBooking();
            }
        }
    }//GEN-LAST:event_confirmActionPerformed

