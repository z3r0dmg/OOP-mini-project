    boolean checkAadhar(String uid) {
        
        boolean not_number=true;
        
        for(int i=0;i<uid.length();i++)
        {
            if(!(uid.charAt(i)>='0' && uid.charAt(i)<='9'))
                return false;
        }
        
        if(uid.length()!=12)
            return false;
        
        return true;
    }
    
    boolean checkPan(String uid) {
        if(uid.length()!=10)
            return false;
        
        for(int i=0;i<5;i++)
        {
            if(!(uid.charAt(i)>='A' && uid.charAt(i)<='Z'))
                return false;
        }
        
        for(int i=5;i<9;i++)
        {
            if(!(uid.charAt(i)>='0' && uid.charAt(i)<='9'))
                return false;
        }
        
        if(!(uid.charAt(9)>='A' && uid.charAt(9)<='Z'))
                return false;
        
        return true;
    }
    
